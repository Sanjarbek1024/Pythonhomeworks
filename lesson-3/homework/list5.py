# Check Element: Given a list and an element, check if the element is present in the list.

list = ['salom', 'hi', 'hello', 'human', 'hajime', 'spasibo', 'good']

element = "hajime"

if element in list:
    print(f"the {element} is available in the given list")
else:
    print(f"Sorry, there is no {element} in this list")