# the integer devision and Finding the remainder

# first choose the numbers
number1 = int(input("Enter the first number: "))
number2 = int(input("Enter the second number: "))

# second

answer = number1 / number2
quotient = number1 // number2
remainder = number1 % number2

# Showing the results

print(f"The answer of the devision is that: {answer}")
print(f"The integer of the devision is that: {quotient}")
print(f" The remainder of the devision is that: {remainder}")